    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here
         if (jComboBox1.getSelectedItem() == null) {
        return;
    }
        try {

        String sql =
        "SELECT t.*, " +
        "p.nama_pelanggan, " +
        "p.telepon, " +
        "m.nama_mobil, " +
        "m.plat_nomor " +
        "FROM transaksi t " +
        "JOIN pelanggan p " +
        "ON t.id_pelanggan = p.id_pelanggan " +
        "JOIN mobil m " +
        "ON t.id_mobil = m.kode_mobil " +
        "WHERE t.id_transaksi = ?";

        PreparedStatement ps =
        conn.prepareStatement(sql);

        ps.setString(
        1,
        jComboBox1.getSelectedItem()
        .toString());

        ResultSet rs =
        ps.executeQuery();

        if(rs.next()) {

            jTextField1.setText(
            rs.getString(
            "id_pelanggan"));

            jTextField2.setText(
            rs.getString(
            "nama_pelanggan"));

            jTextField3.setText(
            rs.getString(
            "telepon"));

            jTextField4.setText(
            rs.getString(
            "id_mobil"));

            jTextField5.setText(
            rs.getString(
            "nama_mobil"));

            jTextField6.setText(
            rs.getString(
            "plat_nomor"));

            jTextField7.setText(
            rs.getString(
            "tgl_sewa"));

            txtTglKembali.setText(
            rs.getString(
            "tgl_kembali"));
        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());
    }
        
    }//GEN-LAST:event_jComboBox1ActionPerformed

