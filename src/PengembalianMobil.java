    private void hitungDenda() {

    try {

        if(txtTglKembali.getText().isEmpty() ||
           jDateChooser1.getDate() == null)
            return;
        SimpleDateFormat sdf =
        new SimpleDateFormat("yyyy-MM-dd");

        Date tglHarusKembali =
        sdf.parse(txtTglKembali.getText());

        Date tglDikembalikan =
        jDateChooser1.getDate();

        long selisih =
        tglDikembalikan.getTime()
        -
        tglHarusKembali.getTime();

        long telat =
        selisih / (1000 * 60 * 60 * 24);

        if(telat < 0)
            telat = 0;

        double denda =
        telat * 50000;

        txtTelat.setText(
        String.valueOf(telat));

        NumberFormat rupiah =
        NumberFormat.getCurrencyInstance(
        new Locale("id","ID"));

        txtDenda.setText(
        rupiah.format(denda));

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());
    }
}
    
    private void loadRental() {

    try {

        jComboBox1.removeAllItems();

        String sql =
        "SELECT id_transaksi " +
        "FROM transaksi " +
        "WHERE status_transaksi='Dipinjam'";
        PreparedStatement ps =
        conn.prepareStatement(sql);

        ResultSet rs =
        ps.executeQuery();

        while(rs.next()) {

            jComboBox1.addItem(
            rs.getString(
            "id_transaksi"));
        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());
    }
}
    
    private void generateIdPengembalian() {

    try {

        String sql =
        "SELECT MAX(id_pengembalian) "
        + "AS terakhir "
        + "FROM pengembalian";

        PreparedStatement ps =
        conn.prepareStatement(sql);

        ResultSet rs =
        ps.executeQuery();

        if(rs.next()) {

            String id =
            rs.getString("terakhir");

            if(id == null) {

                id = "PG001";

            } else {

                int nomor =
                Integer.parseInt(
                id.substring(2));

                nomor++;

                id =
                String.format(
                "PG%03d",
                nomor);
            }

            idPengembalian = id;
        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());
    }
}
    
    private void tampilDataPengembalian() {

    DefaultTableModel model =
    (DefaultTableModel)
    jTable1.getModel();

    model.setRowCount(0);

    try {

        String sql =
        "SELECT * FROM pengembalian";

        PreparedStatement ps =
        conn.prepareStatement(sql);

        ResultSet rs =
        ps.executeQuery();

        NumberFormat rupiah =
        NumberFormat.getCurrencyInstance(
        new Locale("id","ID"));

        while(rs.next()) {

            Object[] data = {

                rs.getString(
                "id_pengembalian"),

                rs.getString(
                "id_transaksi"),

                rs.getDate(
                "tgl_dikembalikan"),

                rs.getInt(
                "telat"),

                rupiah.format(
                rs.getDouble(
                "denda"))
            };

            model.addRow(data);
        }

    } catch(Exception e) {

        JOptionPane.showMessageDialog(
        this,
        e.getMessage());
    }
}


